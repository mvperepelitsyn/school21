#include "../fillit.h"

void		ft_read(int fd, t_atetr **mtetr)
{
    char	buf[21];
    int		j;

    j = 0;
    while (read(fd, buf, 21) > 0)
    {
        maketetr(buf, mtetr, j);
        (*mtetr)->tetr[j]->name = 65 + j;
        j++;
    }
}