#include "../fillit.h"

int		ft_checkconn(char *str)
{
    int			i;
    size_t		count;

    i = 0;
    count = 0;
    while (i < 21)
    {
        if (str[i] == '#')
        {
            if ((i + 1) <= 20 && str[i + 1] == '#')
                count++;
            if ((i + 5) <= 20 && str[i + 5] == '#')
                count++;
            if ((i - 1) >= 0 && str[i - 1] == '#')
                count++;
            if ((i - 5) >= 0 && str[i - 5] == '#')
                count++;
        }
        i++;
    }
    return ((count == 6 || count == 8) ? 1 : 0);
}
