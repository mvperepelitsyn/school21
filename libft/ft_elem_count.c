#include "libft.h"

int	ft_elem_count(int n)
{
	int k;

	k = 0;
	if (n < 0)
		k++;
	if (n == 0)
		k++;
	while (n != 0)
	{
		n = n / 10;
		k++;
	}
	return (k);
}
