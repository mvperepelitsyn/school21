#include "libft.h"

int		ft_sqrt(int nb)
{
	int n;

	n = 1;
	if (nb > 0 && nb <= 2147395600)
	{
		while (nb >= n)
		{
			if (nb / n == n && nb % n == 0)
				return (n);
			n++;
		}
	}
	return (0);
}
