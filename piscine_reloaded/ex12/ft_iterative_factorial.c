int		ft_iterative_factorial(int nb)
{
	int n;
	int l;

	n = 1;
	l = 1;
	if (nb == 0 || nb == 1)
		return (n);
	if (nb <= 12 && nb > 0)
	{
		while (l <= nb)
		{
			n = n * l;
			l++;
		}
		return (n);
	}
	else
		return (0);
}
